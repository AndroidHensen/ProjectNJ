public class MaterialIO {
    private String project_name;
    private String material_name;
    private String material_type;
    private int material_io_count;
    private String material_department;
    private String material_io_date;
    private String material_handler;

    public String getProject_name() {
        return project_name;
    }

    public void setProject_name(String project_name) {
        this.project_name = project_name;
    }

    public String getMaterial_name() {
        return material_name;
    }

    public void setMaterial_name(String material_name) {
        this.material_name = material_name;
    }

    public String getMaterial_type() {
        return material_type;
    }

    public void setMaterial_type(String material_type) {
        this.material_type = material_type;
    }

    public int getMaterial_io_count() {
        return material_io_count;
    }

    public void setMaterial_io_count(int material_io_count) {
        this.material_io_count = material_io_count;
    }

    public String getMaterial_department() {
        return material_department;
    }

    public void setMaterial_department(String material_department) {
        this.material_department = material_department;
    }

    public String getMaterial_io_date() {
        return material_io_date;
    }

    public void setMaterial_io_date(String material_io_date) {
        this.material_io_date = material_io_date;
    }

    public String getMaterial_handler() {
        return material_handler;
    }

    public void setMaterial_handler(String material_handler) {
        this.material_handler = material_handler;
    }
}
